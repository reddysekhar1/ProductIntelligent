package com.intelliservice.agentanalytics.model;

public class Clusteres {
	private String label;
	private Documents documents;
	public String getLabel() {
		return label;
	}
	public Clusteres setLabel(String label) {
		this.label = label;
		return this;
	}
	public Documents getDocuments() {
		return documents;
	}
	public Clusteres setDocuments(Documents documents) {
		this.documents = documents;
		return this;
	}
	

}
